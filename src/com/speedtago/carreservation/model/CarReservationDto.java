package com.speedtago.carreservation.model;

public class CarReservationDto {
	private int res_id;
	private String res_sdate;
	private String res_edate;
	private String res_splace;
	private String res_eplace;
	private String res_price;
	private String id;
	public int getRes_id() {
		return res_id;
	}
	public void setRes_id(int res_id) {
		this.res_id = res_id;
	}
	public String getRes_sdate() {
		return res_sdate;
	}
	public void setRes_sdate(String res_sdate) {
		this.res_sdate = res_sdate;
	}
	public String getRes_edate() {
		return res_edate;
	}
	public void setRes_edate(String res_edate) {
		this.res_edate = res_edate;
	}
	public String getRes_splace() {
		return res_splace;
	}
	public void setRes_splace(String res_splace) {
		this.res_splace = res_splace;
	}
	public String getRes_eplace() {
		return res_eplace;
	}
	public void setRes_eplace(String res_eplace) {
		this.res_eplace = res_eplace;
	}
	public String getRes_price() {
		return res_price;
	}
	public void setRes_price(String res_price) {
		this.res_price = res_price;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	
}
